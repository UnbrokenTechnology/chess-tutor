// =========================================================================
// Fully-expanded table: PSQ[Piece::index()][Square::index()]
// =========================================================================
//
// Each entry combines:
//   - The piece's material value (from `types::Value`), as a Score.
//   - The piece's positional bonus for that square.
//
// White entries are built by indexing the half-board tables with
// (rank, file-folded-to-queenside). Black entries are the white entries
// vertically flipped and sign-inverted, matching how the reference
// post-processes `psq[~pc][~s] = -psq[pc][s]`.

/// Material value of a piece type, as a `Score` (material is the same in
/// both game phases *except* for the precomputed MG/EG piece values).
const fn material_score(pt: PieceType) -> Score {
    match pt {
        PieceType::Pawn => Score::new(Value::PAWN_MG.0, Value::PAWN_EG.0),
        PieceType::Knight => Score::new(Value::KNIGHT_MG.0, Value::KNIGHT_EG.0),
        PieceType::Bishop => Score::new(Value::BISHOP_MG.0, Value::BISHOP_EG.0),
        PieceType::Rook => Score::new(Value::ROOK_MG.0, Value::ROOK_EG.0),
        PieceType::Queen => Score::new(Value::QUEEN_MG.0, Value::QUEEN_EG.0),
        PieceType::King => Score::ZERO, // Kings have no material value in psqt scoring.
    }
}

/// Positional bonus for a white piece of the given type, at the given
/// (rank, file). For all types except pawns the table is file-symmetric,
/// so we fold file into a half-file index first.
const fn white_positional_bonus(pt: PieceType, rank: usize, file: usize) -> Score {
    let half_file = if file < 4 { file } else { 7 - file };
    match pt {
        PieceType::Pawn => PAWN_BONUS[rank][file],
        PieceType::Knight => KNIGHT_BONUS[rank][half_file],
        PieceType::Bishop => BISHOP_BONUS[rank][half_file],
        PieceType::Rook => ROOK_BONUS[rank][half_file],
        PieceType::Queen => QUEEN_BONUS[rank][half_file],
        PieceType::King => KING_BONUS[rank][half_file],
    }
}

const fn build_psq() -> [[Score; 64]; 15] {
    let mut table = [[Score::ZERO; 64]; 15];

    let piece_types: [PieceType; 6] = [
        PieceType::Pawn,
        PieceType::Knight,
        PieceType::Bishop,
        PieceType::Rook,
        PieceType::Queen,
        PieceType::King,
    ];

    let mut i = 0;
    while i < 6 {
        let pt = piece_types[i];
        let material = material_score(pt);

        // Piece indices for white and black versions of `pt`.
        // White pawns = 1, knights = 2, ..., kings = 6.
        // Black pawns = 9, knights = 10, ..., kings = 14.
        let white_index = pt as usize;
        let black_index = (pt as usize) | 8;

        let mut sq_idx = 0usize;
        while sq_idx < 64 {
            let file = sq_idx & 7;
            let rank = sq_idx >> 3;

            // White's score: material + positional from the perspective of
            // (rank, file).
            let bonus = white_positional_bonus(pt, rank, file);
            let white_score = Score(material.0 + bonus.0);
            table[white_index][sq_idx] = white_score;

            // Black's score: same piece on the vertically-flipped square,
            // with both mg and eg sign-inverted. (Vertical flip matches
            // how the reference defines `~s`.)
            let flipped_sq_idx = sq_idx ^ 56;
            table[black_index][flipped_sq_idx] = Score(-white_score.0);

            sq_idx += 1;
        }
        i += 1;
    }

    table
}

/// `PSQ[piece.index()][square.index()]` is the material + positional score
/// of a piece of that kind and color sitting on that square, from white's
/// point of view (so black's contributions are already negative).
pub static PSQ: [[Score; 64]; 15] = build_psq();

// =========================================================================
// Lookup
// =========================================================================

/// Score of this piece being on this square, combining material value and
/// positional preference. White contributes a positive score; black
/// contributes a negative score.
pub fn psq_score(piece: Piece, square: Square) -> Score {
    PSQ[piece.index()][square.index()]
}

