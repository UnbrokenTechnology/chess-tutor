//! Piece-square tables.
//!
//! `psq_score(piece, square)` returns a packed middlegame/endgame `Score`
//! covering both material value and positional preference for the given
//! piece being on that square. Two positions with identical piece
//! placement have the same sum of psq scores — which is the starting
//! point of classical evaluation, and the only component that updates
//! incrementally at O(1) per move.
//!
//! Weights are factual numerical data carried over from the Stockfish 11
//! reference (`reference/Stockfish-sf_11/src/psqt.cpp`). Structure here is
//! freshly authored in Rust:
//!
//! - The per-piece positional tables in the reference are asymmetric by
//!   piece (non-pawns are defined only for files a–d and mirrored, pawns
//!   are asymmetric across the whole board). We unfold everything at
//!   compile time into the flat `PSQ[Piece::index()][Square::index()]`.
//! - Black's entries are white's entries, vertically flipped and negated.
//!   The unfold is done once in a `const fn` builder so there's no init()
//!   to call.

